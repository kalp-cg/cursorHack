const TABLES: Record<Locale, Dict> = { en, hi, gu };

export function t(locale: Locale, key: string, vars?: Record<string, string>): string {
  let s = TABLES[locale]?.[key] ?? TABLES.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replaceAll(`{${k}}`, v);
    }
  }
  return s;
}

/** Dev helper: ensure all locales share the same keys */
export function assertI18nParity(): string[] {
  const keys = Object.keys(en);
  const missing: string[] = [];
  for (const loc of ["hi", "gu"] as Locale[]) {
    for (const k of keys) {
      if (!(k in TABLES[loc])) missing.push(`${loc}.${k}`);
    }
  }
  return missing;
}
